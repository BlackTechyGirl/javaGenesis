package Practice;

public class Encapsulate {
    private String name;
    private int roll;
    private int age;

    public void setName(String name) {
        this.name = name;
    }
    public String getName() {
        return name;
    }
    public void setRoll(int roll) {
        this.roll =roll;
    }
    public int getRoll() {
        return roll;
    }
    public void setAge(int age) {
        this.age = age;
    }
    public int getAge() {
        return age;
    }

}
